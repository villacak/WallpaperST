package au.com.constants;

public enum ScreenHorientation {
	SCREEN_PORTRAIT(1), SCREEN_SENSOR(4);
	
	private int returnCode;


	private ScreenHorientation(int returnCode) {
		this.returnCode = returnCode;
	}


	public String getStrCode() {
		return returnCode + "";
	}
	
	public int getIntCode() {
		return returnCode;
	}

}
